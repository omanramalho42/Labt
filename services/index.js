export const submitComment = async (obj) => {
  try {
    const result = await fetch('/api/comments', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        headers: {
          'Content-Type': 'application/json',
          'Accept': 'application/json',
        },
      },
      body: JSON.stringify(obj),
    });
  
    return result.json();
  } catch (error) {
    throw error;
  }
};

